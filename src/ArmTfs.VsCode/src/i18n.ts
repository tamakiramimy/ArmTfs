export function getUiLanguage(): 'en' | 'zh-CN' {
  const configured = vscode.workspace.getConfiguration('armTfs').get<UiLanguage>('ui.language', 'auto');
  if (configured === 'en' || configured === 'zh-CN') {
    return configured;
  }

  return vscode.env.language.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en';
}

export function t(key: string, params?: Record<string, string | number | undefined>): string {
  const language = getUiLanguage();
  const message = dictionaries[language][key] ?? dictionaries.en[key] ?? key;
  if (!params) {
    return message;
  }

  return message.replace(/\{([^}]+)\}/g, (_, name: string) => `${params[name] ?? ''}`);
}
