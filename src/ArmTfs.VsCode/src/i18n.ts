export function getUiLanguage(): 'en' | 'zh-CN' {
  const configured = getConfigValue<UiLanguage>('ui.language', 'auto');
  if (configured === 'en' || configured === 'zh-CN') {
    return configured;
  }

  return vscode.env.language.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en';
}

export function t(key: string, params?: Record<string, string | number | undefined>): string {
  const language = getUiLanguage();
  const message = dictionaries[language][key] ?? dictionaries.en[key] ?? key;
  if (!params) {
    return message;
  }

  return message.replace(/\{([^}]+)\}/g, (_, name: string) => `${params[name] ?? ''}`);
}

export function translateCliText(text: string): string {
  if (getUiLanguage() !== 'zh-CN' || !text.trim()) {
    return text;
  }

  return text
    .replaceAll(
      "No workspace found. Run 'arm-tfs workspace new' first.",
      "未找到工作区。请先运行 'arm-tfs workspace new'。",
    )
    .replace(
      /No workspace found for path '([^']+)'\. Run 'arm-tfs workspace new' first\./g,
      "路径“$1”没有找到工作区。请先运行 'arm-tfs workspace new'。",
    )
    .replace(
      /Local folder '([^']+)' is already mapped to '([^']+)'\. Choose another folder or remove the existing mapping first\./g,
      "本地目录“$1”已经映射到“$2”。请更换目录，或先移除现有映射。",
    );
}

export function translateCliMessage(message: string): string {
  return translateCliText(message);
}

const changeTypeKeyMap: Record<string, string> = {
  add: 'changeType.add',
  edit: 'changeType.edit',
  delete: 'changeType.delete',
  rename: 'changeType.rename',
  merge: 'changeType.merge',
  branch: 'changeType.branch',
  undelete: 'changeType.undelete',
  rollback: 'changeType.rollback',
  encoding: 'changeType.encoding',
  lock: 'changeType.lock',
  sourcerename: 'changeType.sourceRename',
  targetrename: 'changeType.targetRename',
  sourcedelete: 'changeType.sourceDelete',
  targetdelete: 'changeType.targetDelete',
};

const statusKeyMap: Record<string, string> = {
  pending: 'status.pending',
  modifiednotcheckedout: 'status.modifiedNotCheckedOut',
  conflict: 'status.conflict',
};

export function translateChangeType(changeType: string | undefined): string {
  if (!changeType?.trim()) {
    return '';
  }

  return changeType
    .split(',')
    .map((part) => part.trim())
    .filter(Boolean)
    .map((part) => {
      const translated = changeTypeKeyMap[part.toLowerCase()];
      return translated ? t(translated) : part;
    })
    .join(', ');
}

export function translateStatusLabel(status: string | undefined): string {
  if (!status?.trim()) {
    return '';
  }

  const normalized = status.toLowerCase();
  for (const [key, value] of Object.entries(statusKeyMap)) {
    if (normalized.includes(key)) {
      return t(value);
    }
  }

  return status;
}
